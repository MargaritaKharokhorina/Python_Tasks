def fib(n):
    if n in [1, 2]:   # прописываем логику выхода: если n содержится в списке из элементов 1 и 2
        return 1      # если попали - возвращаем единицу
    else:             # иначе вызываем рекурсивный вызов для n-1 и n-2
        return fib(n - 1) + fib (n - 2)
    
list = []
for e in range(1, 10):
    list.append(fib(e))
print(list)           # [1, 1, 2, 3, 5, 8, 13, 21, 34]